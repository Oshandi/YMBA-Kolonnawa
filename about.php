                                <h3 class="p-text-secondary p-mb-5">
                                    Services
                                </h3>
                                <p class="p-mb-5">
                                    The Young Men’s Buddhist Association, Kolonnawa has launched a wider array of
                                    projects
                                    under four main sub committees, in order to enhance and promote Education, Art,
                                    Literature,
                                    Sports and other life skills for the youth living in Kolonnawa. There are 4 ongoing
                                    long term
                                    projects under the Association: the Suwasetha Medical Center, Sambudhdhaloka
                                    Preschool,
                                    Thisarana Dhamma School and Sucharitha Newspaper. In addition to these projects the
                                    executive committee will organize short term projects to achieve the above mentioned
                                    objectives. Those projects will focus on membership development, enhance skills and
                                    knowledge
                                    of members, religious celebrations and social services.
                                </p>
                                <h4 class="p-text-secondary p-mb-5">
                                    Suwasetha Medical Centre
                                </h4>
                                <p class="p-mb-5">
                                    Suwasetha Medical Center was established on 09th of August, 2004 by Mr. Saliya
                                    Wickramasinghe, president of YMBA. This was the result of a successful project set
                                    up in
                                    August 2001, with shared capital on a 50:50 basis by the YMBA (K) and the individual
                                    members
                                    of the association.
                                </p>
                                <p class="p-mb-5">
                                    This is the main medical center that functions in the Kolonnawa area, offering OPD
                                    Services,
                                    Specialist Channel Services, Pathological Laboratory Services, Dental Clinic, Eye
                                    Care Unit and
                                    a Pharmacy Services at a concessionary rate.
                                </p>
                                <p class="p-mb-5">
                                    The Medical Center provides free services to Buddhist Priests, children with Special
                                    Needs and
                                    senior citizens.
                                </p>

                                <h4 class="p-text-secondary p-mb-5">
                                    Sambuddhaloka Pre-School
                                </h4>
                                <p class="p-mb-5">
                                    Early childhood is the most critical time for positive intervention. Children’s
                                    development during
                                    this stage is strongly affected by their environment, and that effect continues to
                                    exert a strong
                                    influence on the rest of their lives. Thus, as a responsible association in the
                                    society, YMBA,
                                    Kolonnawa operates a preschool for children in need at Orugodawatta, where a
                                    community of
                                    low income families resides. Presently about 100 children have been enrolled each
                                    year and they
                                    are taught and taken care by well trained qualified teaching staff from the three
                                    main
                                    communities: sinhala, tamil & muslim.
                                </p>
                                <p class="p-mb-5">
                                    YMBA (K), with a vision to enlighten the next generation as a future investment for
                                    the well-
                                    being of society, has generously spent around Rs. 30,000.00 per month to operate and
                                    maintain
                                    this facility. Your generous support and donations are most welcome.
                                </p>

                                <h4 class="p-text-secondary p-mb-5">
                                    Thisarana English Dhamma School
                                </h4>
                                <p class="p-mb-5">
                                    The YMBA Kolonnawa commenced the English Medium Dhamma School on 10th of
                                    September, 2005, fulfilling one of the main requirements of the students in
                                    Kolonnawa who do
                                    their studies in the English medium. Currently the school provides this service to
                                    nearly 100
                                    students, with members of the YMBA volunteering to teach in the school.
                                </p>

                                <h4 class="p-text-secondary p-mb-5">
                                    Susiritha Newspaper
                                </h4>
                                <p class="p-mb-5">
                                    The monthly newspaper of the YMBA Kolonnawa was first published in October 2003 and
                                    the
                                    circulation of the paper continued with contributions from the members. The main
                                    focus of this
                                    paper is to provide opportunities for the youth of the community who are interested
                                    in improving
                                    literacy skills and make the Kolonnawa community aware of the services and projects
                                    carried out
                                    by the YMBA. This paper is distributed among families in Kolonnawa free of charge.
                                    In 2021
                                    YMBA Kolonnawa will launch an online newspaper, the first of its kind in Sri Lanka,
                                    that
                                    belongs to a charity organization. This will focus on improving literacy skills of
                                    students and
                                    also including notes, past papers and answers of Buddhism paper for Ordinary Level
                                    Examination and Daham Pasal Awasana Examination.
                                </p>
